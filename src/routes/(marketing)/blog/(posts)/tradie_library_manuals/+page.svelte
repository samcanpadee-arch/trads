<p class="lead">Smart Assistant cites the manuals and standards your crew needs mid-job.</p>

<p>
  When crews upgrade to Pro, the Tradie Library becomes their secret weapon. We ingest installation guides, safety alerts,
  and checklists so Smart Assistant can quote the right section while you’re typing. No more endless scrolling through PDFs on
  mobile.
</p>

<h3>Tag your manuals</h3>
<p>
  Upload documents with plain-language tags: “Daikin install”, “AS/NZS 3000 clause 2.6”. Those tags become search handles and
  make prompts faster. Ask “Reference the AS/NZS clause about RCDs” and the answer includes the excerpt plus page number.
</p>

<h3>Keep answers audited</h3>
<p>
  Every reply logs which manual was cited so auditors and supervisors can review decisions later. Teams share links inside the
  business without exposing files to clients, which keeps IP safe while still answering compliance questions quickly.
</p>

<h3>Blend manuals with your wording</h3>
<p>
  Because Smart Assistant understands your tone of voice from Settings, it can merge raw standards with customer-friendly
  explanations. The result: hazard reports and maintenance quotes that sound like you wrote them—only faster.
</p>

<p>
  Tip: pair the Tradie Library with Saved Prompts so apprentices can pull the same authoritative answer every time.
</p>
